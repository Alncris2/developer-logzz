    //Cria a URL a partir da string sem acentos.
    public function urlCreate($str)
    { 
        $str = $this->removeAccents($str);
        $str = preg_replace(array('/[^a-zA-Z0-9 \'-]/', '/[ -\']+/', '/^-|-$/'), array('', '-', ''), $str);
        $str = preg_replace('/-inc$/i', '', $str);
        $str = $this->makeUniqueURL($str);
        $url = strtolower($str['url']);
        return array('url' => $url, 'unified' => $str['unified']);
    }


    // Adiciona string aleatória no início da URL para torná-la única, caso necessário
    public function makeUniqueURL($url)
    {
        require_once (dirname(__FILE__) . '/../config.php');
        $stmt = $conn->prepare('SELECT * FROM sales WHERE sale_url = :sale_url');
        $stmt->execute(array('sale_url' => $url));

        if ($stmt->rowCount() == 0){
            return array('url' => $url, 'unified' => 0);
            exit;
        }
            do {
                $chars = 'ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz';
                $random_string = str_shuffle($chars);
                $url = substr($random_string, 4,5) . "-" . $url;

                $stmt = $conn->prepare('SELECT * FROM sales WHERE sale_url = :sale_url');
                $stmt->execute(array('sale_url' => $url));
                
            } while ($stmt->rowCount() != 0);

            return array('url' => $url, 'unified' => 1);
    }
